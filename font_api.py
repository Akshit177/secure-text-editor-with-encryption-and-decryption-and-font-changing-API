# -----------------------------
# AVAILABLE STYLE MAP
# -----------------------------
STYLES = {
    "bold": BOLD,
    "italic": ITALIC,
    "calligraphy": CALLIGRAPHY,
    "uppercase": "UPPERCASE",
    "lowercase": "LOWERCASE"
}

# -----------------------------
# STYLE CONVERSION FUNCTION
# -----------------------------
def convert_style(text: str, style: str) -> str:
    if style not in STYLES:
        raise ValueError("Invalid style selected")

    mapping = STYLES[style]

    # Built-in styles
    if mapping == "UPPERCASE":
        return text.upper()
    if mapping == "LOWERCASE":
        return text.lower()

    # Symbol-based styles
    result = ""
    for c in text:
        result += mapping.get(c, c)
    return result
